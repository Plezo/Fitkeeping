import UIKit
import SwiftUI

extension Color {
    public static var maroon: Color {
        return Color(UIColor(red: 144/255, green: 55/255, blue: 73/255, alpha: 1.0))
    }
    
    public static var whiteText: Color {
        return Color(UIColor(red: 255/255, green: 255/255, blue: 255/255, alpha: 1.0))
    }
    
    public static var whiteTextHalf: Color {
        return Color(UIColor(red: 255/255, green: 255/255, blue: 255/255, alpha: 0.5))
    }
    
    public static var bgBlack: Color {
        return Color(UIColor(red: 30/255, green: 32/255, blue: 34/255, alpha: 100))
    }
}
